class Program
{
    static void Main()
    {
        string выбор = "";
        bool играИдет = true;

        Console.WriteLine("Ты просыпаешься. Твоего котика нет. Нужно его найти!");

        // пример цикла while — игра продолжается, пока не наступает конец
        while (играИдет)
        {
            Console.WriteLine("1 - Встать и искать");
            Console.WriteLine("2 - Полежать дальше");
            выбор = Console.ReadLine();

            if (выбор == "2")
            {
                Console.WriteLine("Ты уснул. Котик обиделся и убежал к соседям. Концовка №1 ");
                break;
            }

            // пример цикла for — обход комнат
            string[] комнаты = { "Кухни", "Ванной" };
            for (int i = 0; i < комнаты.Length; i++)
            {
                Console.WriteLine($"Ты начинаешь поиск с {комнаты[i]}?");
                Console.WriteLine("1 - Да");
                Console.WriteLine("2 - Нет");
                выбор = Console.ReadLine();

                if (комнаты[i] == "Кухни" && выбор == "1")
                {
                    Console.WriteLine("На кухне следы лапок.");
                    Console.WriteLine("1 - Проверить под столом");
                    Console.WriteLine("2 - Проверить холодильник");
                    выбор = Console.ReadLine();

                    if (выбор == "1")
                    {
                        Console.WriteLine("Под столом ничего. Позвать котика? (1 - Да / 2 - Нет)");
                        выбор = Console.ReadLine();

                        if (выбор == "1")
                        {
                            Console.WriteLine("Во дворе слышно мяуканье!");
                            Console.WriteLine("1 - Пойти туда");
                            Console.WriteLine("2 - Остаться дома");
                            выбор = Console.ReadLine();

                            if (выбор == "1")
                            {
                                Console.WriteLine("Котик лежит на лавочке и ждет тебя. Концовка №2 ");
                                играИдет = false;
                                break;
                            }
                            else
                            {
                                Console.WriteLine("Котик нашел тебя сам. Концовка №3 ");
                                играИдет = false;
                                break;
                            }
                        }
                        else
                        {
                            Console.WriteLine("В комнате под кроватью кто-то шуршит...");
                            Console.WriteLine("1 - Посмотреть");
                            Console.WriteLine("2 - Убежать");
                            выбор = Console.ReadLine();

                            if (выбор == "1")
                            {
                                Console.WriteLine("Это котик! Он нашёл твой носок. Концовка №4");
                                играИдет = false;
                                break;
                            }
                            else
                            {
                                Console.WriteLine("Ты убежал. Котик очень сильно расстроился и убежал жить к соседям на неделю! Концовка №5");
                                играИдет = false;
                                break;
                            }
                        }
                    }
                    else
                    {
                        Console.WriteLine("В холодильнике только курица. Съесть? (1 - Да / 2 - Нет)");
                        выбор = Console.ReadLine();

                        if (выбор == "1")
                        {
                            Console.WriteLine("Ты съел курицу. Котик обиделся и убежал к соседке, которая всегда будет его хорошо кормить! Концовка №6");
                            играИдет = false;
                            break;
                        }
                        else
                        {
                            Console.WriteLine("Котик прибежал на запах. Концовка №7");
                            играИдет = false;
                            break;
                        }
                    }
                }
                else if (комнаты[i] == "Ванной" && выбор == "1")
                {
                    // пример do...while — проверка нескольких мест
                    int попытка = 0;
                    do
                    {
                        Console.WriteLine("1 - Проверить за шторкой");
                        Console.WriteLine("2 - Проверить стиральную машину");
                        выбор = Console.ReadLine();
                        попытка++;
                    }
                    while (попытка < 1); // одноразовая проверка (пример do...while)

                    if (выбор == "1")
                    {
                        Console.WriteLine("За шторкой никого.");
                        Console.WriteLine("1 - Проверить под раковиной");
                        Console.WriteLine("2 - Выйти на балкон");
                        выбор = Console.ReadLine();

                        if (выбор == "1")
                        {
                            Console.WriteLine("Под раковиной игрушка твоего кота! Ты идешь по следу из ниточек и находишь котика спящим на диване. Концовка №8");
                            играИдет = false;
                            break;
                        }
                        else
                        {
                            Console.WriteLine("Котик на балконе, играет с солнечным зайчиком. Концовка  №9");
                            играИдет = false;
                            break;
                        }
                    }
                    else
                    {
                        Console.WriteLine("В стиральной машине котика нет.");
                        Console.WriteLine("Ты слышишь мяуканье — он в коридоре! Концовка №10 ");
                        играИдет = false;
                        break;
                    }
                }
            }

            // пример foreach — показ всех возможных концовок
            string[] концовки = {
                "1 - Котик обиделся и убежал к соседям ",
                "2 - Котик лежит на лавочке и ждет тебя ",
                "3 - Котик нашел тебя сам ",
                "4 - Котик с носком под кроватью ",
                "5 - Котик очень сильно расстроился и убежал жить к соседям на неделю ",
                "6 - Котик обиделся и убежал к соседке, которая всегда будет его хорошо кормить ",
                "7 - Котик прибежал за курицей ",
                "8 - Котик спит на диване ",
                "9 - Котик на балконе, играет с солнечным зайчиком ",
                "10 - Котик мяукает в коридоре "
            };

            Console.WriteLine("\nВозможные концовки игры:");
            foreach (string конец in концовки)
            {
                Console.WriteLine(конец);
            }

    Console.WriteLine("\nКонец игры ");
            играИдет = false;
        }
    }
}

